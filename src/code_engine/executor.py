@dataclass(frozen=True, slots=True)
class Issue:
    """Static analysis issue."""

    severity: str
    message: str
    line: int = 0
    code: str = "STATIC"

    def to_dict(self) -> dict[str, Any]:
        """Return a JSON-friendly dictionary."""

        return asdict(self)


@dataclass(frozen=True, slots=True)
class ExecutionResult:
    """Result of running code in a bounded subprocess."""

    output: str
    errors: str
    runtime: float
    returncode: int
    timed_out: bool = False

    @property
    def success(self) -> bool:
        """Return true when execution succeeded."""

        return self.returncode == 0 and not self.timed_out and not self.errors.strip()

    def to_dict(self) -> dict[str, Any]:
        """Return a JSON-friendly dictionary."""

        payload = asdict(self)
        payload["success"] = self.success
        return payload


@dataclass(frozen=True, slots=True)
class TestCase:
    """Generated executable test case."""

    name: str
    code: str
    expected: str = "success"

    def to_dict(self) -> dict[str, Any]:
        """Return a JSON-friendly dictionary."""

        return asdict(self)


@dataclass(frozen=True, slots=True)
class CodeResult:
    """Final result returned by BeastCodeEngine."""

    code: str
    tests: list[TestCase]
    output: ExecutionResult
    verified: bool
    issues: list[Issue] = field(default_factory=list)
    language: str = "python"

    def to_dict(self) -> dict[str, Any]:
        """Return a JSON-friendly dictionary."""

        return {
            "code": self.code,
            "tests": [test.to_dict() for test in self.tests],
            "output": self.output.to_dict(),
            "verified": bool(self.verified),
            "issues": [issue.to_dict() for issue in self.issues],
            "language": self.language,
        }


def _normalize_language(language: str) -> str:
    """Normalize language names."""

    if not isinstance(language, str):
        raise TypeError("language must be a string")
    value = language.strip().lower()
    aliases = {"py": "python", "js": "javascript", "ts": "typescript", "cpp": "c++"}
    return aliases.get(value, value or "python")


def _clean_task(task: str) -> str:
    """Normalize task text."""

    if not isinstance(task, str):
        raise TypeError("task must be a string")
    cleaned = re.sub(r"\s+", " ", task.strip())
    if not cleaned:
        raise ValueError("task must not be empty")
    return cleaned


