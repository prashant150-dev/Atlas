class StaticAnalyzer:
    """AST-based Python static analyzer with simple security checks."""

    dangerous_imports = {"os", "subprocess", "socket", "shutil", "ctypes", "winreg", "multiprocessing"}
    dangerous_calls = {"eval", "exec", "compile", "open", "input", "__import__", "breakpoint"}
    dangerous_attrs = {"system", "popen", "remove", "unlink", "rmdir", "rmtree", "rename", "replace"}

    def analyze(self, code: str) -> list[Issue]:
        """Analyze Python code and return issues."""

        if not isinstance(code, str):
            raise TypeError("code must be a string")
        issues: list[Issue] = []
        try:
            tree = ast.parse(code)
        except SyntaxError as exc:
            return [Issue("error", exc.msg, exc.lineno or 0, "SYNTAX")]

        for node in ast.walk(tree):
            if isinstance(node, (ast.Import, ast.ImportFrom)):
                issues.extend(self._check_import(node))
            elif isinstance(node, ast.Call):
                issue = self._check_call(node)
                if issue is not None:
                    issues.append(issue)
            elif isinstance(node, ast.While):
                if isinstance(node.test, ast.Constant) and node.test.value is True:
                    issues.append(Issue("warning", "Possible infinite loop: while True", node.lineno, "LOOP"))

        if not any(isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef)) for node in tree.body):
            issues.append(Issue("info", "Code has no top-level function or class definitions", 1, "STRUCTURE"))
        return issues

    def _check_import(self, node: ast.Import | ast.ImportFrom) -> list[Issue]:
        """Check import nodes."""

        issues: list[Issue] = []
        names = []
        if isinstance(node, ast.Import):
            names = [alias.name.split(".")[0] for alias in node.names]
        elif node.module:
            names = [node.module.split(".")[0]]
        for name in names:
            if name in self.dangerous_imports:
                issues.append(Issue("error", f"Disallowed import: {name}", node.lineno, "SECURITY"))
        return issues

    def _check_call(self, node: ast.Call) -> Issue | None:
        """Check call nodes."""

        if isinstance(node.func, ast.Name) and node.func.id in self.dangerous_calls:
            return Issue("error", f"Disallowed call: {node.func.id}", node.lineno, "SECURITY")
        if isinstance(node.func, ast.Attribute) and node.func.attr in self.dangerous_attrs:
            return Issue("error", f"Disallowed attribute call: {node.func.attr}", node.lineno, "SECURITY")
        return None


class SandboxExecutor:
    """Run Python code in a bounded subprocess."""

    def __init__(self, work_dir: str | Path = "experiments/_code_sandbox") -> None:
        """Create an executor work directory."""

        self.work_dir = Path(work_dir).resolve()
        self.work_dir.mkdir(parents=True, exist_ok=True)
        self.analyzer = StaticAnalyzer()

    def execute(self, code: str, timeout: float = 5) -> ExecutionResult:
        """Execute Python code with timeout after static safety checks."""

        if timeout <= 0:
            raise ValueError("timeout must be positive")
        issues = self.analyzer.analyze(code)
        blocking = [issue for issue in issues if issue.severity == "error"]
        if blocking:
            return ExecutionResult("", "\n".join(issue.message for issue in blocking), 0.0, 2)

        script_path = self.work_dir / f"run_{uuid.uuid4().hex}.py"
        script_path.write_text(code, encoding="utf-8")
        env = {
            "PYTHONIOENCODING": "utf-8",
            "PYTHONDONTWRITEBYTECODE": "1",
            "PATH": os.environ.get("PATH", ""),
            "SYSTEMROOT": os.environ.get("SYSTEMROOT", ""),
            "WINDIR": os.environ.get("WINDIR", ""),
        }
        started = time.perf_counter()
        try:
            completed = subprocess.run(
                [sys.executable, "-I", "-B", str(script_path)],
                cwd=self.work_dir,
                env=env,
                capture_output=True,
                text=True,
                timeout=float(timeout),
            )
            runtime = time.perf_counter() - started
            return ExecutionResult(
                output=completed.stdout,
                errors=completed.stderr,
                runtime=runtime,
                returncode=int(completed.returncode),
                timed_out=False,
            )
        except subprocess.TimeoutExpired as exc:
            runtime = time.perf_counter() - started
            return ExecutionResult(
                output=exc.stdout or "",
                errors=f"Execution timed out after {timeout} seconds",
                runtime=runtime,
                returncode=124,
                timed_out=True,
            )
        finally:
            try:
                script_path.unlink()
            except OSError as exc:
                cleanup_note = str(exc)
                _ = cleanup_note


class TestGenerator:
    """Generate focused Python tests for generated code."""

    def generate_tests(self, code: str, task: str) -> list[TestCase]:
        """Generate executable tests."""

        cleaned = _clean_task(task)
        lowered = cleaned.lower()
        tests: list[TestCase] = [TestCase("syntax_import", self._wrap_test(code, "assert callable(globals().get('solve'))"))]

        if "factorial" in lowered:
            tests.append(TestCase("factorial_cases", self._wrap_test(code, "assert factorial(0) == 1\nassert factorial(5) == 120\nassert solve(6) == 720")))
        elif "fibonacci" in lowered or "fib" in lowered:
            tests.append(TestCase("fibonacci_cases", self._wrap_test(code, "assert fibonacci(0) == 0\nassert fibonacci(1) == 1\nassert fibonacci(7) == 13")))
        elif "palindrome" in lowered:
            tests.append(TestCase("palindrome_cases", self._wrap_test(code, "assert is_palindrome('Racecar') is True\nassert is_palindrome('hello') is False")))
        elif "sort" in lowered:
            tests.append(TestCase("sort_cases", self._wrap_test(code, "assert sort_numbers([3, 1, 2]) == [1, 2, 3]")))
        elif any(phrase in lowered for phrase in ("add", "sum")):
            tests.append(TestCase("sum_cases", self._wrap_test(code, "assert add(2, 3) == 5\nassert sum_numbers([1, 2, 3]) == 6")))
        else:
            tests.append(TestCase("generic_case", self._wrap_test(code, "marker = object()\nassert solve(marker) is marker")))
        return tests

    def _wrap_test(self, code: str, assertions: str) -> str:
        """Wrap solution code with assertions."""

        return f"{code.rstrip()}\n\n{assertions}\nprint('test ok')\n"


class SelfHealingLoop:
    """Apply small deterministic fixes for common generated-code errors."""

    def __init__(self, generator: CodeGenerator | None = None, max_iterations: int = 3) -> None:
        """Create a healing loop."""

        if max_iterations <= 0:
            raise ValueError("max_iterations must be positive")
        self.generator = generator or CodeGenerator()
        self.max_iterations = int(max_iterations)

    def heal(self, code: str, errors: str, task: str = "", language: str = "python") -> str:
        """Return fixed code when a known issue can be repaired."""

        healed = code
        for _ in range(self.max_iterations):
            lower_errors = errors.lower()
            if "syntaxerror" in lower_errors:
                healed = self.generator.generate(task or "generic identity function", language)
            elif "nameerror" in lower_errors and "math" in lower_errors and "import math" not in healed:
                healed = "import math\n" + healed
            elif "assertionerror" in lower_errors and task:
                healed = self.generator.generate(task, language)
            else:
                return healed
        return healed


class BeastCodeEngine:
    """High-level code generation, analysis, execution, testing, and healing."""

    def __init__(
        self,
        generator: CodeGenerator | None = None,
        analyzer: StaticAnalyzer | None = None,
        executor: SandboxExecutor | None = None,
        test_generator: TestGenerator | None = None,
        healer: SelfHealingLoop | None = None,
    ) -> None:
        """Create the full code engine."""

        self.generator = generator or CodeGenerator()
        self.analyzer = analyzer or StaticAnalyzer()
        self.executor = executor or SandboxExecutor()
        self.test_generator = test_generator or TestGenerator()
        self.healer = healer or SelfHealingLoop(self.generator)

    def solve(self, task: str, language: str = "python") -> CodeResult:
        """Generate code, test it, and return verified code or honest failure."""

        normalized_language = _normalize_language(language)
        code = self.generator.generate(task, normalized_language)
        issues = self.analyzer.analyze(code) if normalized_language == "python" else []
        if normalized_language != "python":
            return CodeResult(
                code=code,
                tests=[],
                output=ExecutionResult("", f"Execution for {normalized_language} is not available locally", 0.0, 3),
                verified=False,
                issues=issues,
                language=normalized_language,
            )

        tests = self.test_generator.generate_tests(code, task)
        output = self._run_tests(tests)
        attempts = 0
        while not output.success and attempts < 3:
            attempts += 1
            code = self.healer.heal(code, output.errors, task=task, language=normalized_language)
            issues = self.analyzer.analyze(code)
            if any(issue.severity == "error" for issue in issues):
                break
            tests = self.test_generator.generate_tests(code, task)
            output = self._run_tests(tests)

        verified = output.success and not any(issue.severity == "error" for issue in issues)
        return CodeResult(code=code, tests=tests, output=output, verified=verified, issues=issues, language=normalized_language)

    def _run_tests(self, tests: Sequence[TestCase]) -> ExecutionResult:
        """Run generated tests and combine outputs."""

        outputs: list[str] = []
        errors: list[str] = []
        runtime = 0.0
        for test in tests:
            result = self.executor.execute(test.code, timeout=5)
            runtime += result.runtime
            if result.output:
                outputs.append(f"[{test.name}]\n{result.output.strip()}")
            if not result.success:
                errors.append(f"[{test.name}]\n{result.errors.strip() or 'test failed'}")
                return ExecutionResult("\n".join(outputs), "\n".join(errors), runtime, result.returncode, result.timed_out)
        return ExecutionResult("\n".join(outputs), "", runtime, 0, False)


def _self_test() -> None:
    """Run a small CPU sanity check for the code engine."""

    engine = BeastCodeEngine()
    factorial = engine.solve("write a factorial function")
    palindrome = engine.solve("write a palindrome checker")
    unsafe = engine.executor.execute("import os\nos.system('echo unsafe')\n")
    javascript = engine.solve("identity function", language="javascript")

    if not factorial.verified or "factorial" not in factorial.code:
        raise RuntimeError(f"Factorial result was not verified: {factorial.to_dict()}")
    if not palindrome.verified:
        raise RuntimeError(f"Palindrome result was not verified: {palindrome.to_dict()}")
    if unsafe.returncode == 0:
        raise RuntimeError("Unsafe code unexpectedly executed")
    if javascript.verified:
        raise RuntimeError("JavaScript should not be marked locally verified")

    print("AetherCore code engine self-test")
    print(f"  factorial verified: {factorial.verified}")
    print(f"  palindrome verified: {palindrome.verified}")
    print(f"  unsafe errors: {unsafe.errors}")
    print(f"  javascript verified: {javascript.verified}")
    print("  status: ok")


if __name__ == "__main__":
    _self_test()
